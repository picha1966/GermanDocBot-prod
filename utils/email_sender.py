def _send_via_resend(
    to_email: str,
    subject: str,
    body_html: str,
    pdf_path: str,
    pdf_filename: str,
) -> bool:
    if not RESEND_AVAILABLE:
        logger.error(
            "EMAIL_PROVIDER_ERROR: provider=resend missing_module=True "
            "— install with: pip install resend"
        )
        return False

    _api_key = os.getenv("RESEND_API_KEY", "")
    logger.info("RESEND_API_KEY_PRESENT=%s", bool(_api_key))
    if not _api_key:
        logger.error("EMAIL_SEND_ERROR: RESEND_API_KEY is not set — cannot send email")
        return False

    try:
        import resend
        import base64

        resend.api_key = _api_key

        with open(pdf_path, "rb") as f:
            pdf_data = base64.b64encode(f.read()).decode()

        logger.info("EMAIL_SEND_START resend to=%s file=%s size=%d", to_email, pdf_filename, len(pdf_data))

        response = resend.Emails.send({
            "from": _get_from_address(),
            "to": [to_email],
            "subject": subject,
            "html": body_html,
            "attachments": [{
                "filename": pdf_filename,
                "content": pdf_data,
            }],
        })

        logger.info("RESEND_RESPONSE=%s", response)
        logger.info("EMAIL_SEND_SUCCESS resend to=%s file=%s", to_email, pdf_filename)
        return True

    except Exception as e:
        logger.error("EMAIL_SEND_ERROR resend to=%s err=%s", to_email, e)
        return False


# ---------------------------------------------------------------------------
# Backend: SMTP (stdlib — no extra dependencies)
# ---------------------------------------------------------------------------

def _send_via_smtp(
    to_email: str,
    subject: str,
    body_html: str,
    body_plain: str,
    pdf_path: str,
    pdf_filename: str,
) -> bool:
    try:
        import smtplib
        from email.mime.multipart import MIMEMultipart
        from email.mime.text import MIMEText
        from email.mime.application import MIMEApplication

        host = os.environ["EMAIL_SMTP_HOST"]
        port = int(os.environ.get("EMAIL_SMTP_PORT", "587"))
        user = os.environ["EMAIL_SMTP_USER"]
        password = os.environ["EMAIL_SMTP_PASS"]
        # For SMTP the From address must match the authenticated account (Gmail strict).
        # Use the SMTP user directly; fall back to EMAIL_FROM only for custom servers.
        _smtp_from_env = _get_from_address()
        if "gmail.com" in host.lower() or _smtp_from_env in ("onboarding@resend.dev",):
            from_addr = f"CivicAssistBot <{user}>"
        else:
            from_addr = _smtp_from_env

        logger.info("SMTP_CONFIG host=%s port=%s user=%s from=%s", host, port, user, from_addr)

        _attach_size = os.path.getsize(pdf_path) if os.path.exists(pdf_path) else 0
        logger.info("ATTACHMENT_PATH=%s exists=%s size=%d", pdf_path, os.path.exists(pdf_path), _attach_size)

        msg = MIMEMultipart("mixed")
        msg["Subject"] = subject
        msg["From"] = from_addr
        msg["To"] = to_email

        # Multipart/alternative: HTML + plain text
        alt = MIMEMultipart("alternative")
        alt.attach(MIMEText(body_plain, "plain", "utf-8"))
        alt.attach(MIMEText(body_html, "html", "utf-8"))
        msg.attach(alt)

        with open(pdf_path, "rb") as f:
            part = MIMEApplication(f.read(), _subtype="pdf")
            part.add_header("Content-Disposition", "attachment", filename=pdf_filename)
            msg.attach(part)

        with smtplib.SMTP(host, port, timeout=10) as server:
            server.ehlo()
            server.starttls()
            server.ehlo()
            server.login(user, password)
            try:
                server.send_message(msg)
            except Exception as _smtp_send_err:
                logger.error("EMAIL_FAILED_FULL=%s", repr(_smtp_send_err))
                raise

        logger.info("EMAIL_SMTP_SENT: to=%s file=%s host=%s", to_email, pdf_filename, host)
        return True
    except Exception as e:
        logger.error("EMAIL_FAILED: %s", repr(e))
        return False


