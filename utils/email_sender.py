def _get_doc_label(doc_type: str, lang: str) -> str:
    labels = _DOC_LABELS.get((doc_type or "").lower(), {})
    return labels.get(lang) or labels.get("en") or doc_type


def _get_official_link(doc_type: str) -> str:
    return _OFFICIAL_LINKS.get((doc_type or "").lower(), _DEFAULT_OFFICIAL_LINK)


def _get_from_address() -> str:
    return os.environ.get("EMAIL_FROM", "CivicAssistBot <noreply@civicassistbot.com>")


def _get_support_link(doc_type: str = "") -> str:
    bot_username = os.environ.get("BOT_USERNAME", "")
    if bot_username and doc_type:
        return f'https://t.me/{bot_username}?start=ai_{doc_type}'
    if bot_username:
        return f'https://t.me/{bot_username}'
    return os.environ.get("SUPPORT_TELEGRAM", "https://t.me/CivicAssistBot")


