def _send_pdf_sync(
    to_email: str,
    pdf_path: str,
    doc_type: str,
    lang: str,
    extra_note: str = "",
) -> bool:
    """Synchronous PDF email delivery. Auto-selects backend from env vars."""
    _lang = "uk" if lang in ("ua", "uk") else lang
    if _lang not in _HTML_L10N:
        _lang = "en"

    doc_label    = _get_doc_label(doc_type, _lang)
    official_link = _get_official_link(doc_type)
    support_href  = _get_support_link(doc_type)

    subject    = _SUBJECT.get(_lang, _SUBJECT["en"])
    body_html  = _build_html_email(_lang, doc_label, official_link, support_href)
    body_plain = _build_plain_email(_lang, doc_label, official_link)
    if extra_note:
        body_plain += f"\n\n{extra_note}"

    pdf_filename = f"{doc_type}_filled.pdf"

    # Priority: SendGrid → Resend (with SMTP fallback) → SMTP
    if os.environ.get("SENDGRID_API_KEY"):
        return _send_via_sendgrid(to_email, subject, body_html, body_plain, pdf_path, pdf_filename)

    if os.environ.get("RESEND_API_KEY"):
        if not RESEND_AVAILABLE:
            logger.warning(
                "EMAIL_PROVIDER_ERROR: provider=resend missing_module=True "
                "— RESEND_API_KEY is set but the resend package is not installed. "
                "Falling back to SMTP if configured."
            )
            if os.environ.get("EMAIL_SMTP_HOST"):
                logger.info("EMAIL_RESEND_FALLBACK_SMTP: resend unavailable, attempting SMTP")
                return _send_via_smtp(to_email, subject, body_html, body_plain, pdf_path, pdf_filename)
            logger.warning(
                "EMAIL_DELIVERY_IMPOSSIBLE: provider=resend missing_module=True "
                "and no SMTP fallback configured — email not sent to %s", to_email
            )
            return False
        _resend_ok = _send_via_resend(to_email, subject, body_html, pdf_path, pdf_filename)
        if _resend_ok:
            return True
        # Resend failed (e.g. unverified domain) — try SMTP fallback if configured
        if os.environ.get("EMAIL_SMTP_HOST") and os.environ.get("EMAIL_SMTP_PASS"):
            logger.warning(
                "EMAIL_RESEND_FAILED_SMTP_FALLBACK: to=%s — Resend failed, trying SMTP", to_email
            )
            return _send_via_smtp(to_email, subject, body_html, body_plain, pdf_path, pdf_filename)
        return False

    if os.environ.get("EMAIL_SMTP_HOST") and os.environ.get("EMAIL_SMTP_PASS"):
        return _send_via_smtp(to_email, subject, body_html, body_plain, pdf_path, pdf_filename)

    logger.warning(
        "EMAIL_NOT_CONFIGURED: no SENDGRID_API_KEY / RESEND_API_KEY / EMAIL_SMTP_HOST set — "
        "skipping email delivery for %s", to_email
    )
    return False


async def send_pdf_by_email(
    to_email: str,
    pdf_path: str,
    doc_type: str,
    lang: str = "en",
    extra_note: str = "",
) -> bool:
    """
    Async wrapper — runs sync email delivery in a thread pool so it never
    blocks the event loop.

    Returns True on success, False on any failure (never raises).
    """
    from utils.lang import normalize_lang
    lang = normalize_lang(lang)

    if not to_email or "@" not in to_email:
        logger.debug("EMAIL_SKIP: invalid or empty email address %r", to_email)
        return False

    if not pdf_path or not os.path.exists(pdf_path):
        logger.warning("EMAIL_SKIP: PDF file not found at %r", pdf_path)
        return False

    try:
        import functools
        loop = asyncio.get_event_loop()
        result = await asyncio.wait_for(
            loop.run_in_executor(
                None,
                functools.partial(_send_pdf_sync, to_email, pdf_path, doc_type, lang, extra_note),
            ),
            timeout=30.0,
        )
        return result
    except asyncio.TimeoutError:
        logger.warning("EMAIL_TIMEOUT: to=%s — provider did not respond within 30s", to_email)
        return False
    except Exception as e:
        logger.warning("EMAIL_UNEXPECTED_ERROR: to=%s err=%s", to_email, e)
        return False


